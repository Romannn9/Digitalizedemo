// ── helpers ──────────────────────────────────────────────────────────────────
function find_page_by_template(string $tpl): ?int {
    $pages = get_pages(['meta_key' => '_wp_page_template', 'meta_value' => $tpl]);
    return $pages ? $pages[0]->ID : null;
}

function seed(int $pid, string $key, $value): void {
    $ok = update_field($key, $value, $pid);
    echo ($ok ? '✓' : '·') . " [$pid] $key\n";
}

function seed_repeater(int $pid, string $key, array $rows): void {
    $ok = update_field($key, $rows, $pid);
    echo ($ok ? '✓' : '·') . " [$pid] $key (" . count($rows) . " rows)\n";
}

