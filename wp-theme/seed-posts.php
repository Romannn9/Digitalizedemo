foreach ($posts as $data) {
    // Перевіряємо чи пост вже існує
    $existing = get_page_by_title($data['title'], OBJECT, 'post');
    if ($existing) {
        echo "SKIP (exists): {$data['title']}\n";
        continue;
    }

    $post_id = wp_insert_post([
        'post_title'   => $data['title'],
        'post_content' => $data['content'],
        'post_excerpt' => $data['excerpt'],
        'post_status'  => 'publish',
        'post_author'  => $author_id,
        'post_date'    => $data['date'],
        'post_type'    => 'post',
    ]);

    if (is_wp_error($post_id)) {
        echo "ERROR: {$data['title']} — " . $post_id->get_error_message() . "\n";
        continue;
    }

    if (isset($cats[$data['category']])) {
        wp_set_post_categories($post_id, [$cats[$data['category']]]);
    }

    echo "✓ Created post ID $post_id: {$data['title']}\n";
}

